package Daytwo2project;

public class Switch {

	public static void main(String[] args) {
		int i=3;
		switch(i)
		{
		case 1:
			System.out.println("1");
		break;
		case 2:
			System.out.println("2");
			break;
		case 3:
			System.out.println("Case Matched,3");
			break;
		case 4:
			System.out.println("4");
			break;
		default:
			System.out.println("No case is matched");
			
		}
		
			

	}

}
